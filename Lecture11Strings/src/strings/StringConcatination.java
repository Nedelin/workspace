package strings;

public class StringConcatination {

	public static void main(String[] args) {
		String firstName  = "Petyr";
		String familyName = "Ivanov";
		
		System.out.println(firstName + " "+ familyName);

		String indexOfTest = "indexOfTest";
		System.out.println(indexOfTest.indexOf("index"));
		System.out.println(indexOfTest.indexOf("Test"));
	}

}
